
// PRIMATIVE DATA TYPES IN C++

#include <iostream>
using namespace std;

int main() {
    int age = 25; // This is a variable declaration and initialization                     -- 4 bytes
    char grade = 'A'; // This is another variable declaration and initialization           -- 1 byte
    float height = 5.9; // This is yet another variable declaration and initialization     -- 4 bytes
    double weight = 150.5; // This is another variable declaration and initialization      -- 8 bytes
    bool isStudent = true; // This is another variable declaration and initialization      -- 1 byte

    cout << "Age: " << age << endl; // Output the value of age
    cout << "Grade: " << grade << endl; // Output the value of grade        
    cout << "Height: " << height << endl; // Output the value of height
    cout << "Weight: " << weight << endl; // Output the value of weight
    cout << "Is Student: " << isStudent << endl; // Output the value of isStudent

    
    
    return 0;
} 


